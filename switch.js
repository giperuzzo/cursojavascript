// switch ele verifica a condição verdadeira e faz o log de acordo com os casos, importante colocar o break para sair do lloop caso seja verdadeiro
// exemplo ele compara o caso = case com a variavel declaradad

const car = "Tesla";

switch (car) {
    case "Mercedes":
        console.log("Mercedes is beautifull!");
        break;
    case "Ferrari":
        console.log("Ferrari is red color!");
        break;
    case "Tesla":
        console.log("Tesla is very tecnologic");
        break;
}